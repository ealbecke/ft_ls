#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	d;
	size_t	x;
	char	*s2;

	s2 = (char*)s;
	x = 0;
	d = (char)c;
	while (x < n)
	{
		s2[x] = d;
		x++;
	}
	return (s);
}
