#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int c;

	c = 0;
	while (src && src[c] != '\0')
	{
		dest[c] = src[c];
		c++;
	}
	dest[c] = '\0';
	return (dest);
}
